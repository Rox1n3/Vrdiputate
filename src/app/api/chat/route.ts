async function requestWithModelFallback(
  openai: OpenAI,
  prompt: string,
  lang: Lang,
  detailed: boolean,
  context?: ChatContext
): Promise<string> {
  const effectivePrompt = buildEffectivePrompt(prompt, detailed, context);
  const configuredModel = getOpenAIModel();
  const models = Array.from(new Set([configuredModel, 'gpt-4o-mini', 'gpt-4.1-mini', 'gpt-5-nano']));
  let lastError: unknown = null;

  for (const model of models) {
    try {
      const completion = await openai.chat.completions.create({
        model,
        messages: [
          { role: 'system', content: systemPrompt(lang, detailed) },
          { role: 'user', content: effectivePrompt }
        ],
        temperature: 0.2
      });

      const text = completion.choices[0]?.message?.content?.trim();
      if (text) return text;
    } catch (err) {
      lastError = err;
    }
  }

  throw lastError ?? new Error('No model produced an answer');
}

export async function POST(req: Request) {
  try {
    const body = await req.json();
    const prompt = typeof body?.prompt === 'string' ? body.prompt.trim() : '';
    const mode = body?.mode;
    const lang = normalizeLang(body?.lang);
    const context: ChatContext | undefined =
      body?.context && typeof body.context === 'object' ? (body.context as ChatContext) : undefined;

    if (!prompt) {
      return NextResponse.json({ error: 'Пустой запрос' }, { status: 400 });
    }

    const isGuest = mode === 'guest';
    if (!isGuest) {
      const session = await readSession();
      if (!session) {
        return NextResponse.json({ error: 'Нужен вход' }, { status: 401 });
      }
    }

    try {
      const openai = getOpenAIClient();
      const detailed = wantsDetailedAnswer(prompt);
      const answer = await requestWithModelFallback(openai, prompt, lang, detailed, context);
      return NextResponse.json({ answer: answer || fallbackText(lang) });
    } catch (err) {
      console.error('[api/chat] OpenAI request failed:', err);
      return NextResponse.json({ error: getErrorText(err, lang) }, { status: 502 });
    }
  } catch (err) {
    console.error('[api/chat] Unexpected server error:', err);
    return NextResponse.json({ error: 'Ошибка на сервере' }, { status: 500 });
  }
}
