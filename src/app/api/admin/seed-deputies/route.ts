export async function POST() {
  const session = await readSession();
  if (!session) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

  const userDoc = await adminDb.collection('users').doc(session.uid).get();
  if (userDoc.data()?.role !== 'admin') {
    return NextResponse.json({ error: 'Forbidden' }, { status: 403 });
  }

  const results: Array<Record<string, unknown>> = [];

  for (const dep of DEPUTIES_TO_CREATE) {
    try {
      // Проверяем, существует ли уже пользователь
      let uid: string;
      try {
        const existing = await adminAuth.getUserByEmail(dep.email);
        uid = existing.uid;
      } catch {
        // Создаём нового пользователя в Firebase Auth
        const user = await adminAuth.createUser({
          email: dep.email,
          password: dep.password,
          displayName: dep.name,
        });
        uid = user.uid;
      }

      // Устанавливаем роль admin в коллекции users
      await adminDb.collection('users').doc(uid).set(
        { role: 'admin' },
        { merge: true }
      );

      // Создаём / обновляем запись о депутате с точными адресами.
      // merge: true — не затирает createdAt и другие существующие поля.
      const deputyRef = adminDb.collection('deputies').doc(uid);
      const existingDeputy = await deputyRef.get();
      await deputyRef.set(
        {
          uid,
          name: dep.name,
          okrug: dep.okrug,
          email: dep.email,
          districts: dep.districts,
          ...(existingDeputy.exists ? {} : { createdAt: new Date().toISOString() }),
        },
        { merge: true }
      );

      results.push({
        name: dep.name,
        email: dep.email,
        okrug: dep.okrug,
        districtsCount: dep.districts.length,
        uid,
        status: 'ok',
      });
    } catch (err: unknown) {
      results.push({
        name: dep.name,
        email: dep.email,
        error: err instanceof Error ? err.message : String(err),
        status: 'error',
      });
    }
  }

  return NextResponse.json({ results });
}
